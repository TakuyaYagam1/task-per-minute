#!/usr/bin/env python3
"""Merge per-domain OpenAPI schema YAMLs into a single components/schemas.yml.

Inputs:  internal/openapi/components/schemas/*.yml (any number of files,
         each providing a top-level mapping of schema names -> definitions).
Output:  internal/openapi/components/schemas.yml - one merged mapping that the
         main openapi.yml references via $ref.

Duplicate schema names across files are a hard error: name collisions silently
dropped by a "last writer wins" merge are exactly the bug class this script
exists to prevent.
"""

from __future__ import annotations

import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    sys.stderr.write(
        "merge-schemas.py: PyYAML is required. Install with: pip install pyyaml\n"
    )
    sys.exit(2)


REPO_ROOT = Path(__file__).resolve().parent.parent
SRC_DIR = REPO_ROOT / "internal" / "openapi" / "components" / "schemas"
DEST_FILE = REPO_ROOT / "internal" / "openapi" / "components" / "schemas.yml"


def main() -> int:
    if not SRC_DIR.is_dir():
        sys.stderr.write(f"merge-schemas.py: source dir not found: {SRC_DIR}\n")
        return 1

    sources = sorted(SRC_DIR.glob("*.yml")) + sorted(SRC_DIR.glob("*.yaml"))
    if not sources:
        sys.stderr.write(f"merge-schemas.py: no *.yml files in {SRC_DIR}\n")
        # Still emit an empty schemas.yml so codegen doesn't break.
        DEST_FILE.write_text("# generated by scripts/merge-schemas.py\n{}\n", "utf-8")
        return 0

    merged: dict[str, object] = {}
    for path in sources:
        with path.open("r", encoding="utf-8") as fh:
            doc = yaml.safe_load(fh) or {}
        if not isinstance(doc, dict):
            sys.stderr.write(
                f"merge-schemas.py: {path} must be a mapping, got {type(doc).__name__}\n"
            )
            return 1
        for name, schema in doc.items():
            if name in merged:
                sys.stderr.write(
                    f"merge-schemas.py: duplicate schema '{name}' "
                    f"(first in another file, also in {path.name})\n"
                )
                return 1
            merged[name] = schema

    DEST_FILE.parent.mkdir(parents=True, exist_ok=True)
    with DEST_FILE.open("w", encoding="utf-8") as fh:
        fh.write("# generated by scripts/merge-schemas.py - do not edit by hand\n")
        yaml.safe_dump(merged, fh, sort_keys=True, allow_unicode=True)

    sys.stdout.write(
        f"merge-schemas.py: merged {len(sources)} file(s), "
        f"{len(merged)} schema(s) -> {DEST_FILE.relative_to(REPO_ROOT)}\n"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
